    def __init__(self, code, model=1):
        if model == 1 or model >= 10:
            # Should this be trapped so that we can return None?
            # Values >= 10 are described as "superstash" so are probably
            # atmospheric variables. Used in some Met Office ancillary files
            try:
                var = atm_stashvar[code]
            except KeyError:
                var = ["UNKNOWN VARIABLE","", "", "", ""]
        else:
            raise "Model type %d not supported at the moment" % model
        self.long_name = var[0]
        # Should there be a dictionary somewhere so this returns a unique
        # set of names? unique as an optional argument?
        if var[1]:
            self.name = var[1]
        else:
            self.name = "field%d" % code
        self.units = var[2]
        self.standard_name = var[3]
        self.code = code
        if var[4]:
            self.uniquename = var[4]
        else:
            self.uniquename = self.name
                                

